
function docshisCena1Escolha() {
	var cena1escolha = ""

	while (true) {
		cena1escolha = prompt('Escolha 1 ou 2')

		if (cena1escolha == 1) {
			window.location.href = "./startlose.html"
		} else if (cena1escolha == 2) {
			window.location.href = "./middle.html"
		} else { 
			alert("Certeza que colocou uma opção válida?")
		}
	break
	}
}

